package se.SkyLax.MPT.Events;

public class MouseProperties {
	
	private int mouseX;
	private int mouseY;
	
	
	public int getMouseY() {
		return mouseY;
	}
	public void setMouseY(int mouseY) {
		this.mouseY = mouseY;
	}
	public int getMouseX() {
		return mouseX;
	}
	public void setMouseX(int mouseX) {
		this.mouseX = mouseX;
	}
	

	
	

}
